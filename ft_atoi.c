#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	nb;
	int				sign;

	while (ft_isspace(*str))
		str++;
	sign = 1;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	nb = 0;
	while (*str >= '0' && *str <= '9')
	{
		nb = 10 * nb + *str - '0';
		str++;
	}
	return (nb * sign);
}
